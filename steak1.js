function Switch1() {
  let p1f = document.getElementById("prod1f");
  let p1b = document.getElementById("prod1b");
  let p1bm = document.getElementById("prod1bM");
  let p1fm = document.getElementById("prod1fM");
  
  if (p1f.style.display = "block") {
    p1f.style.display = "none";
    p1b.style.display = "block";
    p1bm.style.display = "none";
    p1fm.style.display = "block";
  }
  }
  
